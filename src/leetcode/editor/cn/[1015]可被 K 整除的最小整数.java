    public int smallestRepunitDivByK(int k) {
        int E = 10 % k;
        Set<Integer> set = new HashSet<>();
        for (int pre = 1%k, len = 1;; len++) {
            if (pre == 0)
                return len;
            else if (set.contains(pre))
                return -1;

            set.add(pre);
            pre = (pre*E + 1) % k;
        }
    }

