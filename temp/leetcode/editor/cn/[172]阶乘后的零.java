    public int trailingZeroes(int n) {
        int twoCount = 0, fiveCount = 0;
        for (int i = 2; i <= n; i++) {
            int num = i;
            while (num > 0 && num % 2 == 0) {
                num = num / 2;
                twoCount++;
            }
            while (num > 0 && num % 5 == 0) {
                num = num / 5;
                fiveCount++;
            }
        }
        return Math.min(twoCount, fiveCount);
    }
