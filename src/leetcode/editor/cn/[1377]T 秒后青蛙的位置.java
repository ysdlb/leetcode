    public double frogPosition(int n, int[][] edges, int t, int target) {
        List<Integer>[] tree = new ArrayList[n+1];
        Arrays.setAll(tree, e -> new ArrayList<>());
        // å“¨å…µ, 1 æ˜¯æ ¹èŠ‚ç‚¹ï¼Œå®ƒæ²¡æœ‰çˆ¶èŠ‚ç‚¹; è€Œå…¶ä»–çš„å­èŠ‚ç‚¹åˆ—è¡¨ä¸­ä¸€å®šå­˜åœ¨è¯¥èŠ‚ç‚¹çš„çˆ¶èŠ‚ç‚¹.
        tree[1].add(0);
        for (int[] edge: edges) {
            int from = edge[0], to = edge[1];
            tree[from].add(to);
            tree[to].add(from);
        }

        long dfs = dfs(tree, 0, 1, t, target);
        return dfs == 0 ? 0.0d : 1.0d/dfs;
    }

    private long dfs(List<Integer>[] tree, int fa, int cur, int t, int target) {
        // å¦‚æœè¿˜èƒ½è·³ï¼Œå°±ä¸€å®šç»§ç»­è·³ï¼›æ˜¯ä¸å¯èƒ½åœåœ¨åŸåœ°çš„
        if (t == 0) return cur == target ? 1 : 0;
        // å¦‚æœæ²¡æœ‰å­©å­ï¼Œä¸èƒ½è·³äº†ï¼Œé‚£ä¹ˆå°±ä¸€ç›´åœåœ¨åŸåœ°ï¼›æ¯ä¸ª tree å…ƒç´ é‡Œä¸€å®šå­˜åœ¨ä¸€ä¸ªçˆ¶èŠ‚ç‚¹
        if (cur == target) return tree[cur].size() == 1 ? 1 : 0;

        for (int e: tree[cur]) {
            if (e == fa) continue;
            long v = dfs(tree, cur, e, t-1, target);
            if (v != 0) // é‡Œé¢ä¸€å®šæœ‰ä¸€ä¸ªæ˜¯ fa èŠ‚ç‚¹
                return v * (tree[cur].size()-1);
        }
        return 0;
    }

